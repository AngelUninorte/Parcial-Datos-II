    private void conectarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_conectarActionPerformed
        String ip = this.ip.getText();
        try {
            System.out.println(ip.length());
            if (ip.length() == 0) {
                JOptionPane.showMessageDialog(null, "No se ingres贸 ip - Conectando con localhost");
                ip = "localhost";
            }
            
            cliente = new Cliente(this, ip);
            cliente.start();
            
            this.ip.setVisible(false);
            this.conectar.setVisible(false);
            this.text.setText("Conectado a " + ip + "... Esperando Respuesta del HOST");
            this.panel1.setBackground(Color.green);
            this.panel2.setBackground(Color.green);
        } catch (IOException ex) {
            Logger.getLogger(ClientPanel.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "No se pudo establecer la conexi贸n con " + ip);
            
            this.panel1.setBackground(Color.red);
            this.panel2.setBackground(Color.red);
            
        }
        
        
        
    }//GEN-LAST:event_conectarActionPerformed


